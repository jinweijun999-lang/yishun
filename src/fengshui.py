def get_zodiac_from_year(year: int) -> str:
    """根据出生年获取属相"""
    zodiac_list = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪']
    return zodiac_list[(year - 1900) % 12]

def analyze_fengshui(year: int, month: int, day: int, shengxiao: str = None) -> dict:
    """风水分析主函数"""
    # 如果没有提供属相，根据出生年推算
    if not shengxiao:
        shengxiao = get_zodiac_from_year(year)
    
    # 年干支（简化版）
    gan = TIANGAN[(year - 4) % 10]
    zhi = DIZHI[(year - 4) % 12]
    
    # 流年九宫飞星
    current_year = 2026  # 实际应该用当前年份
    feiwuxing = NIANJIA_FEIWUXING.get(current_year, NIANJIA_FEIWUXING[2026])
    
    # 属相吉凶方位
    fangwei = SHUXIANG_FANGWEI.get(shengxiao, SHUXIANG_FANGWEI['鼠'])
    
    return {
        'shengxiao': shengxiao,
        'niangan': gan,
        'nianzhi': zhi,
        'feiwuxing': feiwuxing,
        'ji_fangwei': fangwei['吉位'],
        'xiong_fangwei': fangwei['凶位'],
        'suspended': {
            'description': '流年财位',
            'recommendations': ['正财位保持整洁', '偏财位摆放招财物', '避免财位受压']
        }
    }

def generate_fengshui_report(year: int, month: int, day: int, shengxiao: str = None) -> str:
    """生成风水报告"""
    result = analyze_fengshui(year, month, day, shengxiao)
    
    report = f"""
🏠 风水堪舆报告
{'='*30}

🐾 属相：{result['shengxiao']}
📅 年柱：{result['niangan']}{result['nianzhi']}

⭐ 流年九宫飞星：
"""
    for star, position in result['feiwuxing'].items():
        report += f"  {star}：{position}\n"
    
    report += f"""
🌟 属相{result['shengxiao']}吉凶方位：
  ✅ 吉利方位：{', '.join(result['ji_fangwei'])}
  ❌ 凶险方位：{', '.join(result['xiong_fangwei'])}

📋 风水调理建议：
"""
    for rec in result['suspended']['recommendations']:
        report += f"  • {rec}\n"
    
    return report

